#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	while (*s)
		s++;
	if ((char)c == '\0')
		return ((char *)s);
	s--;
	while (*s != (char)c && *s)
		s--;
	if (*s == (char)c)
		return ((char *)s);
	return (NULL);
}
