#include "libft.h"

char	*ft_strmapi(const char *s, char (*f) (unsigned int, char))
{
	int		i;
	char	*str;
	size_t	size;

	i = 0;
	size = ft_strlen((char *)s);
	str = (char *)ft_memalloc(sizeof(char) * size + 1);
	if (str == NULL)
		return (NULL);
	while (s[i])
	{
		str[i] = f(i, s[i]);
		i++;
	}
	str[i] = 0;
	return (str);
}
